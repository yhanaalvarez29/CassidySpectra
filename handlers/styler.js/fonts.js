const FontSystem = {
  applyFonts: function (text, font = "none") {
    const formattedText = text
      .split("")
      .map((char) => fonts[font][char] || char)
      .join("");
    return formattedText;
  },
  allFonts: function () {
    let fontList = ``;
    Object.keys(fonts).forEach((font) => {
      const someText = font
        .replace(/_/g, " ")
        .replace(/\b\w/g, (match) => match.toUpperCase());

      fontList += this.fonts[font](someText) + "\n";
    });
    return fontList;
  },
  fontMap: fonts,
  get fonts() {
    return new Proxy(
      { ...fonts },
      {
        get(target, prop) {
          if (prop in target) {
            return function (text) {
              return FontSystem.applyFonts(String(text), prop);
            };
          } else {
            return (i) => i;
          }
        },
      }
    );
  },
};
if (process.argv[2] === "list") {
  require("fs").writeFileSync(__dirname + "/font.txt", FontSystem.allFonts());
}

module.exports = FontSystem;
